/** Normalize APPLE_ASC_PRIVATE_KEY from env (supports escaped \\n and quoted PEM). */
export function normalizeAscPrivateKey(raw: string): string {
  let key = raw.trim();
  if (
    (key.startsWith('"') && key.endsWith('"')) ||
    (key.startsWith("'") && key.endsWith("'"))
  ) {
    key = key.slice(1, -1);
  }
  return key.replace(/\\n/g, "\n");
}
