import type { FlagshipGuideContent } from "@/lib/commanders";

export type CommanderGuideLane = {
  title: string;
  kicker: string;
  cards: string[];
};

export type HandBuiltCommanderGuide = {
  slug: string;
  name: string;
  shortName: string;
  archetype: string;
  loveReason: string;
  bestFor: string;
  pills: string[];
  firstUpgrade: string;
  budgetFirstUpgrade: string;
  winPaths: string[];
  traps: string[];
  tableReputation: string;
  bestLanes: CommanderGuideLane[];
  budgetLanes: CommanderGuideLane[];
};

function lane(title: string, kicker: string, cards: string[]): CommanderGuideLane {
  return { title, kicker, cards };
}

function guide(seed: HandBuiltCommanderGuide): HandBuiltCommanderGuide {
  return seed;
}

export function flagshipContentForGuide(guideSeed: HandBuiltCommanderGuide): FlagshipGuideContent {
  const upgradePriority = guideSeed.budgetLanes.map((item, index) => `${index + 1}. ${item.title}`);
  return {
    loveReason: guideSeed.loveReason,
    bestFor: guideSeed.bestFor,
    winPaths: guideSeed.winPaths,
    traps: guideSeed.traps,
    upgradePriority,
    openingPlan: [
      `Turn 1: develop mana or the cheapest enabler for ${guideSeed.archetype}.`,
      `Turn 2: add protection, card flow, or the support piece that makes ${guideSeed.shortName} matter.`,
      `Turn 3+: commit the commander or bridge toward the first engine turn only when the board can use it.`,
    ],
    tableReputation: guideSeed.tableReputation,
    communityHeadline: `How people build ${guideSeed.shortName} now`,
    communitySubhead: `Compare public ${guideSeed.shortName} lists by engines, payoff density, and upgrade lanes.`,
  };
}

