#!/usr/bin/env node
/**
 * Generate SQL INSERT statements for precon_decks from Westly/CommanderPrecons.
 * Run: node frontend/scripts/generate-precon-sql.mjs
 * Output: precon_decks_all.sql (or stdout with --stdout)
 *
 * Fetches all Commander precon JSON files from GitHub, parses them, and generates
 * INSERT statements for the precon_decks table.
 */

const GITHUB_API = "https://api.github.com/repos/Westly/CommanderPrecons/contents/precon_json";
const RAW_BASE = "https://raw.githubusercontent.com/Westly/CommanderPrecons/main/precon_json";

function escapeSql(str) {
  if (str == null || str === "") return "''";
  return "'" + String(str).replace(/'/g, "''").replace(/\\/g, "\\\\") + "'";
}

function escapeSqlArray(arr) {
  if (!Array.isArray(arr) || arr.length === 0) return "'{}'";
  const escaped = arr.map((c) => escapeSql(c)).join(",");
  return `ARRAY[${escaped}]::text[]`;
}

/** Parse deck name like "Arcane Maelstrom (Commander 2020 Precon Decklist)" to extract set name and year */
function parseDeckName(name) {
  const match = name.match(/\s*\((.+?)\)\s*$/);
  const setPart = match ? match[1] : "";
  const yearMatch = setPart.match(/\d{4}/);
  const year = yearMatch ? parseInt(yearMatch[0], 10) : new Date().getFullYear();
  const set_name = setPart.replace(/\s*Precon\s*Decklist\s*$/i, "").trim() || setPart || "Unknown";
  return { set_name, release_year: year };
}

/** Convert Moxfield precon JSON to precon_decks row */
function moxfieldToPrecon(json) {
  const { name, main, mainboard } = json;
  if (!name || !main) return null;

  const commanderName = main.name || main;
  const colorIdentity = main.color_identity || main.colors || [];
  const colors = Array.isArray(colorIdentity) ? colorIdentity : [];

  const { set_name, release_year } = parseDeckName(name);
  const deckName = name.replace(/\s*\(.+\)\s*$/, "").trim() || name;

  const lines = [];
  lines.push(`1 ${commanderName}`);

  if (mainboard && typeof mainboard === "object") {
    for (const [cardName, entry] of Object.entries(mainboard)) {
      const qty = entry?.quantity ?? entry?.card?.quantity ?? 1;
      if (cardName && qty > 0) {
        lines.push(`${qty} ${cardName}`);
      }
    }
  }

  const deck_text = lines.join("\n");
  return {
    name: deckName,
    commander: commanderName,
    colors,
    format: "Commander",
    deck_text,
    set_name,
    release_year,
  };
}

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status}: ${url}`);
  return res.json();
}

async function main() {
  const args = process.argv.slice(2);
  const stdout = args.includes("--stdout");
  const limit = args.find((a) => a.startsWith("--limit="));
  const maxFiles = limit ? parseInt(limit.split("=")[1], 10) : 9999;

  console.error("Fetching precon file list from GitHub...");
  const files = await fetchJson(GITHUB_API);
  const jsonFiles = files.filter((f) => f.name?.endsWith(".json"));

  console.error(`Found ${jsonFiles.length} precon JSON files. Processing...`);

  const sqlLines = [
    "-- Generated by scripts/generate-precon-sql.mjs",
    "-- Source: https://github.com/Westly/CommanderPrecons",
    "-- Run in Supabase SQL Editor",
    "",
    "INSERT INTO public.precon_decks (name, commander, colors, format, deck_text, set_name, release_year)",
    "VALUES",
  ];

  const values = [];
  let count = 0;
  let errCount = 0;

  for (const file of jsonFiles) {
    if (count >= maxFiles) break;
    const url = `${RAW_BASE}/${encodeURIComponent(file.name)}`;
    try {
      const json = await fetchJson(url);
      const precon = moxfieldToPrecon(json);
      if (!precon || !precon.deck_text) {
        console.error(`  Skip (no deck_text): ${file.name}`);
        continue;
      }
      const dt = precon.deck_text;
      const tag = "p" + Math.random().toString(36).slice(2, 10);
      const colorsSql = escapeSqlArray(precon.colors);
      values.push(
        `  (${escapeSql(precon.name)}, ${escapeSql(precon.commander)}, ${colorsSql}, ${escapeSql(precon.format)}, $${tag}$${dt}$${tag}$, ${escapeSql(precon.set_name)}, ${precon.release_year})`
      );
      count++;
      if (count % 10 === 0) console.error(`  Processed ${count}...`);
    } catch (e) {
      errCount++;
      console.error(`  Error ${file.name}: ${e.message}`);
    }
  }

  sqlLines.push(values.join(",\n") + ";");
  sqlLines.push("");
  sqlLines.push(`-- ${count} precons generated. ${errCount} errors.`);
  sqlLines.push("-- To avoid duplicates, run: DELETE FROM precon_decks; before re-importing.");

  const sql = sqlLines.join("\n");

  if (stdout) {
    process.stdout.write(sql);
  } else {
    const fs = await import("fs");
    const path = await import("path");
    const outPath = path.join(process.cwd(), "precon_decks_all.sql");
    fs.writeFileSync(outPath, sql, "utf8");
    console.error(`\nWrote ${count} precons to ${outPath}`);
    console.error("Run this SQL in Supabase Dashboard → SQL Editor");
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
